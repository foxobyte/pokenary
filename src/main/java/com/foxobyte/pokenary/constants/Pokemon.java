    private Integer defense;
    private Integer specialAttack;
    private Integer specialDefense;
    private Integer speed;

    Pokemon() {
        Properties properties = new Properties();

        try {
            properties.load(new FileInputStream("./src/main/resources/pokemon/" + this.name() + ".properties"));
            this.nationalNumber = Integer.parseInt(properties.getProperty("nationalNumber"));
            this.type = Arrays.stream(properties.getProperty("type").replace(" ", "").toUpperCase().split(",")).map(Type::valueOf).collect(Collectors.toSet());
            this.pokemonName = properties.getProperty("name");
            this.hp = Integer.parseInt(properties.getProperty("hp"));
            this.attack = Integer.parseInt(properties.getProperty("attack"));
            this.defense = Integer.parseInt(properties.getProperty("defense"));
            this.specialAttack = Integer.parseInt(properties.getProperty("spAtk"));
            this.specialDefense = Integer.parseInt(properties.getProperty("spDef"));
            this.speed = Integer.parseInt(properties.getProperty("speed"));
        } catch (FileNotFoundException e) {
            System.out.println("FileNotFoundException: File not found for pokemon: " + this.name()  + "\n" + e.getLocalizedMessage());
        } catch (IOException e) {
            System.out.println("IOException: Error reading file for pokemon: " + this.name()  + "\n" + e.getLocalizedMessage());
        } catch (IllegalArgumentException e) {
            System.out.println("IllegalArgumentException: Error reading property for pokemon: " + this.name() + "\n" + e.getLocalizedMessage());
        } catch (Exception e) {
            System.out.println("SOMETHIGN WENT WRONG " + e.getLocalizedMessage());
        }
    }

